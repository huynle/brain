func TestOpenCodeTarget_Uninstall_RemovesPluginFiles(t *testing.T) {
	tmpDir := t.TempDir()
	configPath := filepath.Join(tmpDir, ".config", "opencode")
	pluginDir := filepath.Join(configPath, "plugin")

	// Create plugin directory with files
	if err := os.MkdirAll(pluginDir, 0755); err != nil {
		t.Fatalf("Failed to create plugin dir: %v", err)
	}

	testFiles := []string{"brain.ts", "brain-planning.ts"}
	for _, filename := range testFiles {
		filePath := filepath.Join(pluginDir, filename)
		if err := os.WriteFile(filePath, []byte("test"), 0644); err != nil {
			t.Fatalf("Failed to create test file: %v", err)
		}
	}

	target := &OpenCodeTarget{
		configPath: configPath,
	}

	err := target.Uninstall()
	if err != nil {
		t.Fatalf("Uninstall() failed: %v", err)
	}

	// Check that files were removed
	for _, filename := range testFiles {
		filePath := filepath.Join(pluginDir, filename)
		if _, err := os.Stat(filePath); !os.IsNotExist(err) {
			t.Errorf("Uninstall() did not remove %s", filename)
		}
	}
}

