func TestOpenCodeTarget_Install_DryRunDoesNotCreateFiles(t *testing.T) {
	tmpDir := t.TempDir()
	configPath := filepath.Join(tmpDir, ".config", "opencode")

	target := &OpenCodeTarget{
		configPath: configPath,
	}

	opts := InstallOptions{
		DryRun: true,
	}
	err := target.Install(opts)

	if err != nil {
		t.Fatalf("Install() with DryRun failed: %v", err)
	}

	// Plugin directory may be created, but files should not be
	pluginDir := filepath.Join(configPath, "plugin")

	// Check that no plugin files were created
	entries, err := os.ReadDir(pluginDir)
	if err != nil {
		// Directory might not exist in dry-run, that's ok
		if !os.IsNotExist(err) {
			t.Fatalf("Failed to read plugin dir: %v", err)
		}
		return
	}

	for _, entry := range entries {
		if strings.HasSuffix(entry.Name(), ".ts") {
			t.Errorf("DryRun should not create file: %s", entry.Name())
		}
	}
}

func TestOpenCodeTarget_Install_ForceOverwritesExistingFiles(t *testing.T) {
	tmpDir := t.TempDir()
	configPath := filepath.Join(tmpDir, ".config", "opencode")
	pluginDir := filepath.Join(configPath, "plugin")

	// Create plugin directory and an existing file
	if err := os.MkdirAll(pluginDir, 0755); err != nil {
		t.Fatalf("Failed to create plugin dir: %v", err)
	}

	existingFile := filepath.Join(pluginDir, "brain.ts")
	existingContent := "// Old content"
	if err := os.WriteFile(existingFile, []byte(existingContent), 0644); err != nil {
		t.Fatalf("Failed to create existing file: %v", err)
	}

	target := &OpenCodeTarget{
		configPath: configPath,
	}

	opts := InstallOptions{
		Force: true,
	}
	err := target.Install(opts)

	if err != nil {
		t.Fatalf("Install() with Force failed: %v", err)
	}

	// Check that file was overwritten
	content, err := os.ReadFile(existingFile)
	if err != nil {
		t.Fatalf("Failed to read file after install: %v", err)
	}

	if string(content) == existingContent {
		t.Error("Force install should overwrite existing file")
	}
}

